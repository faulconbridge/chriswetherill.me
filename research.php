					<h2>Semantic satiation among lexically ambiguous words</h2>
					<p>Semantic satiation research indicates that weakly-related semantic information is more satiated than highly-related information (Balota & Black, 1997). In the current studies, we used biased ambiguous words to investigate possible differences in satiation and the duration of satiation. Participants read ambiguous cues 3 or 30 times and either immediately or after a delay made a CUE&mdash;TARGET relatedness judgment. Targets were consistent with the dominant or subordinate meaning or unrelated to either. Experiments 1 and 2 satiatied noun-noun homographs (e.g., calf). Experiment 2 included a delayed relatedness judgment and indicated that satiation becomes extinct after no more than one minute (contrary to, e.g., Kuhl & Anderson, 2011). Experiment 3 also satiated noun-verb homographs (e.g., duck). Evidence of satiation was found among the immediate response tasks. Experiment 3 supported the theory that greater semantic distance exists between alternative meanings of noun-verb ambiguous words compared with noun-noun ambiguous words (Mirman, Strauss, Dixon, & Magnuson, 2010).</p>
